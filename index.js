async function mint() {

    const minting = document.getElementById('minting')
    minting.innerHTML = "Process Started. You will be notified After Minting is done."
    const contractAddress = "0x1128e9Ecbe082b35478CAb255f4c8968A8812F1d";
    const companyAddress = "0x55E428bfE81f3bF994CE1E3E5f09df49FA38ECee";

    const privateKey = new ethereumjs.Buffer.Buffer("private key", 'hex');

    const web3 = new Web3("https://rinkeby.infura.io/v3/6d17d1d302fd468a9ccc16233e5ff1b8");

    web3.eth.defaultAccount = companyAddress;

    var myContract = new web3.eth.Contract(contractAbi, contractAddress);

    const myAccount = window.userWalletAddress

    const query = await myContract.methods.mint(myAccount).encodeABI();

    web3.eth.getTransactionCount(companyAddress, (err, txCount) => {
        // Build the transaction
        const txObject = {
            nonce: web3.utils.toHex(txCount),
            to: contractAddress,
            value: web3.utils.toHex(web3.utils.toWei('0', 'ether')),
            gasLimit: web3.utils.toHex(2100000),
            gasPrice: web3.utils.toHex(web3.utils.toWei('60', 'gwei')),
            data: query
        }
        // Sign the transaction
        const tx = new ethereumjs.Tx(txObject, { chain: 'rinkeby' });
        tx.sign(privateKey);

        const serializedTx = tx.serialize();
        const raw = '0x' + serializedTx.toString('hex');

        const transaction = web3.eth.sendSignedTransaction(raw).then(tx => {
            console.log('Tx:', tx);
            minting.innerHTML = "Your NTF is Minted!"
        })
            .catch(e => {
                console.error('Error broadcasting the transaction: ', e);
                alert('Error broadcasting the transaction: ' + e)
            });



    })
}



function toggleButton() {
    console.log("start - Toggle");
    const loginButton = document.getElementById('loginButton')
    const mintButton = document.getElementById('mintButton')

    if (!window.ethereum) {
        loginButton.innerText = 'MetaMask is not installed'
        loginButton.classList.remove('bg-purple-500', 'text-white')
        loginButton.classList.add('bg-gray-500', 'text-gray-100', 'cursor-not-allowed')
        return false
    }

    loginButton.addEventListener('click', loginWithMetaMask)

    mintButton.classList.remove('bg-purple-500', 'text-white')
    mintButton.classList.add('bg-gray-500', 'text-gray-100', 'cursor-not-allowed')

    console.log("end - Toggle");
}

async function loginWithMetaMask() {
    console.log("start - login");
    const userWallet = document.getElementById('userWallet')

    console.log("pressed");
    const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' })
        .catch((e) => {
            console.error(e.message)
            return
        })
    if (!accounts) { return }


    window.userWalletAddress = accounts[0]

    userWallet.innerText = window.userWalletAddress
    loginButton.innerText = 'Sign out of MetaMask'

    const mintButton = document.getElementById('mintButton')

    mintButton.classList.remove('bg-gray-500', 'text-gray-100', 'cursor-not-allowed')
    mintButton.classList.add('bg-blue-500', 'text-white')

    loginButton.removeEventListener('click', loginWithMetaMask)
    setTimeout(() => {
        loginButton.addEventListener('click', signOutOfMetaMask)
    }, 200)

    console.log("end - login");
}

function signOutOfMetaMask() {
    window.userWalletAddress = null
    userWallet.innerText = ''
    loginButton.innerText = 'Sign in with MetaMask'

    const mintButton = document.getElementById('mintButton')

    mintButton.classList.remove('bg-purple-500', 'text-white')
    mintButton.classList.add('bg-gray-500', 'text-gray-100', 'cursor-not-allowed')

    loginButton.removeEventListener('click', signOutOfMetaMask)
    setTimeout(() => {
        loginButton.addEventListener('click', loginWithMetaMask)
    }, 200)
}